let liIndex = 0
let limax = document.querySelectorAll('.app img').length-1

function showItem(){
    document.querySelectorAll('img')
        .forEach(e=> 
            e.className = e.className.replace('hide', ''))

  let needTo_hideli = document.querySelectorAll('.app img')[liIndex]
  needTo_hideli.className = needTo_hideli.className.replace('show', 'hide')

  if (liIndex >= limax) {
      liIndex = -1
  }

  let needTo_showli = document.querySelectorAll('img')[++liIndex]
  needTo_showli.className = 'show'
  
}
